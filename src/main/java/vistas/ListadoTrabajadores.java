  private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
    // Esto nos dice el numero de fila seleccionado
    int indice = tblTrabajadores.getSelectedRow();
    // obtenemos del arraylist el trabajador mediante su indice
    Trabajador trabajador = this.trabajadores.get(indice);
    
    TrabajadorDAO tDAO = new TrabajadorDAO();
    
    
    try {
      if(tDAO.eliminarTrabajador(trabajador.getRut())){
        JOptionPane.showMessageDialog(this, "Eliminado correctamente");
      } else {
        JOptionPane.showMessageDialog(this, "No se ha podido eliminar");
      }
    } catch (SQLException ex) {
      System.out.println(ex.getMessage());
    }
    
    this.listar();
  }//GEN-LAST:event_btnEliminarActionPerformed

  private void listar(){
    TrabajadorDAO tDAO = new TrabajadorDAO();
    
    try {
      
      
      if(txtRut.getText().isEmpty()){
        trabajadores = tDAO.listarTrabajadores();
      } else {
        trabajadores = tDAO.buscarPorRut(txtRut.getText());
        
        if(trabajadores.size() == 0){
          JOptionPane.showMessageDialog(this, "No se ha encontrado nada con el RUT: " + txtRut.getText());
        }
      }
      
      DefaultTableModel modelo = new DefaultTableModel();
      modelo.addColumn("rut");
      modelo.addColumn("nombre");
      modelo.addColumn("apellido");
      modelo.addColumn("edad");
      modelo.addColumn("cargo");
      modelo.addColumn("sexo");
      modelo.addColumn("esExtranjero");
      
      for(Trabajador trabajadore : trabajadores){
        String[] fila = new String[7];
        fila[0] = trabajadore.getRut();
        fila[1] = trabajadore.getNombre();
        fila[2] = trabajadore.getApellido();
        fila[3] = String.valueOf(trabajadore.getEdad());
        fila[4] = trabajadore.getCargo();
        if(trabajadore.isSexo()){
          fila[5] = "masculino";
        } else {
          fila[5] = "femenino";
        }
        if(trabajadore.isExtranjero()){
          fila[6] = "Si";
        } else {
          fila[6] = "No";
        }
        modelo.addRow(fila);
      }
      
      tblTrabajadores.setModel(modelo);
      
    } catch (SQLException ex) {
      System.out.println(ex.getMessage());
    }
  }
